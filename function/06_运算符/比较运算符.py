m = 10
n = 20

print('等于: ')
print(m == n)

print('不等于:')
print(m != n)
# print(m <> n) 过时

print('大于:')
print(m > n)

print('小于:')
print(m < n)

print('大于等于:')
print(m >= n)

print('小于等于:')
print(m <= n)





