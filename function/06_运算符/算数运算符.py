# 算数运算符
"""
字符串:
    num1 = '张三'
    num2 = "李四"
"""
num1 = '张三'
num2 = "李四"
num = num1 + num2   # 字符串加法,相当于字符串拼接
numX = num1 * 5     # 字符串乘法,相当于执行多次
print(num)
print(numX)

"""
数字
    age = 5  
"""
n = 3
m = 5
print(n+n)      # 数字加法
print(m-n)      # 数字减法
print(m*n)      # 数字乘法
print(m/n)      # 数字除法
print(m//n)     # 数字取商
print(m % n)    # 数字取模
print(m**n)     # 数字平法
